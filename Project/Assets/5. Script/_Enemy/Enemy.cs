using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Enemy : MonoBehaviour
{
    [SerializeField]
    float maxEnemyHP = 50; // ???????? ????HP
    float nowEnemy; // ???????? ????HP

    [SerializeField]
    Player player; // ?????????? ?????? ???????? ???? ????
    [SerializeField]
    float detectionRange = 10.0f; // ???????? ?????????? ?????? ????

    Animator Anime;
    Transform target;
    UnityEngine.AI.NavMeshAgent nav;

    [SerializeField]
    GameObject attackEffectPrefab;

    [SerializeField]
    float attackTime = 4f;
    float timer = 0f;
    void Start()
    {
        timer = attackTime;
        player = Player.GetInstance;
        nowEnemy = maxEnemyHP;
        Anime = GetComponent<Animator>();
        //GameObject obj = GameObject.FindGameObjectWithTag("Player");
        //target = obj.transform;
        nav = GetComponent<UnityEngine.AI.NavMeshAgent>();
       // nav.acceleration = Mathf.Infinity;
    }

    void Update()
    {
        // ?????????? ?????? ?????? ???? ????
        float distanceToPlayer = Vector3.Distance(transform.position, player.transform.position);

        if (nav.enabled == true)
        {
            // ?????????? ???? ???? ???? ?????? ?????????? ???? ????
            timer += Time.deltaTime;
            if (distanceToPlayer <= detectionRange)
            {
                Anime.SetBool("IsChasing", true);
                nav.SetDestination(player.transform.position);
                if (nav.remainingDistance <= nav.stoppingDistance)
                {
                    if (timer >= attackTime)
                    {
                        transform.LookAt(player.transform);
                        Anime.SetTrigger("IsAttack");
                        timer = 0f;
                    }
                }
            }else
            {
                nav.SetDestination(transform.position);
                Anime.SetBool("IsChasing", false);
            }
        }
    }

    public void ActivateAttackEffect()
    {
        // ???????? ???????? ???????? ???????? ?????? ??????????.
        Vector3 effectPosition = player.transform.position + Vector3.forward; // ???????? ?????? ????
        Instantiate(attackEffectPrefab, effectPosition, Quaternion.identity);

        // ???????? ?????? ???? ?????? ?????? ?? ????????.
    }


    private void OnTriggerEnter(Collider other)
    {
        Debug.Log(other.gameObject) ;
    }
}





